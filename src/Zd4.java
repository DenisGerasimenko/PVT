public class Zd4 {
    public static void main(String[] args) {
        double a;
        double b;
        double r;
        double c;
        a=3;
        b=6;
        c=Math.sqrt(a*a+b*b);
        r=5;
        if (c >= 2*r) {
            System.out.println("Прямоугольное отверстие не закрывается круглой картонкой");
        }else
            System.out.println("Прямоугольное отверстие закрывается круглой картонкой");

    }
}
